EMOJI = {'red': ':red_circle:', 'yellow': ':large_yellow_circle:',
         'green': ':large_green_circle:', 'done': ':white_check_mark:'}
EMOJI_TXT = {'red': '🔴', 'yellow': '🟡', 'green': '🟢', 'done': '✅'}
STATUS_LABEL = {'red': 'At Risk', 'yellow': 'Watch', 'green': 'On Track', 'done': 'Shipped'}


