def slack_card(bucket):
    """Slack mrkdwn card for one milestone/project bucket."""
    m = bucket['metrics']
    status = bucket['status']
    label = STATUS_LABEL[status]
    emoji = EMOJI[status]
    header = bucket['project']
    if bucket['milestone']:
        header += f" — {bucket['milestone']}"

    lines = [f"*{emoji} {header}*  (`{label}`)"]

    # Metrics line
    metric_parts = [f"{m['open']} open / {m['total']} total"]
    if m['overdue']:
        metric_parts.append(f"{m['overdue']} overdue")
    if m['soon'] and status != 'red':
        metric_parts.append(f"{m['soon']} due ≤3d")
    if m['earliest_due']:
        metric_parts.append(f"next due {m['earliest_due']}")
    lines.append("   " + "  ·  ".join(metric_parts))

    # Impact
    lines.append(f"   *Why it matters:* {bucket['impact']}")

    # Reasons (only if not green)
    if status != 'green' and bucket['reasons']:
        lines.append(f"   *Signal:* {'; '.join(bucket['reasons'])}")

    # Top 2 tickets that drive status (overdue first, then soon)
    refs = []
    for issue in bucket.get('open_issues', []):
        if not issue.get('dueDate'):
            continue
        refs.append((issue['dueDate'], issue))
    refs.sort(key=lambda x: x[0])
    if refs:
        for _, issue in refs[:2]:
            lines.append(f"   └─ <{issue['url']}|{issue['id']}> "
                         f"[{issue['status']}] {issue['title'][:80]}  `due {issue['dueDate']}`")
    else:
        lines.append(f"   └─ <https://linear.app/testbox|view in Linear>")

    return "\n".join(lines)


def render_slack(health):
    out = []
    # by TSA
    by_tsa = defaultdict(list)
    for b in health['buckets']:
        by_tsa[b['tsa_name']].append(b)

    out.append(f"*📊 Scrum of Scrums — Milestone Health Digest*")
    out.append(f"_{health['date']} · source of truth: Linear · full details in each ticket_\n")

    # Top summary
    by_status = health['by_status']
    out.append(f"*Team rollup:*  "
               f"🔴 {by_status.get('red', 0)} at risk  ·  "
               f"🟡 {by_status.get('yellow', 0)} watch  ·  "
               f"🟢 {by_status.get('green', 0)} on track  ·  "
               f"✅ {by_status.get('done', 0)} shipped (last 14d)\n")

    for tsa in sorted(by_tsa.keys()):
        cards = by_tsa[tsa]
        # Ignore TSAs who only have "done" or zero
        active = [c for c in cards if c['status'] != 'done']
        if not cards:
            continue
        # header
        reds = sum(1 for c in cards if c['status'] == 'red')
        yellows = sum(1 for c in cards if c['status'] == 'yellow')
        summary_parts = []
        if reds: summary_parts.append(f"🔴 {reds}")
        if yellows: summary_parts.append(f"🟡 {yellows}")
        greens = sum(1 for c in cards if c['status'] == 'green')
        if greens: summary_parts.append(f"🟢 {greens}")
        dones = sum(1 for c in cards if c['status'] == 'done')
        if dones: summary_parts.append(f"✅ {dones}")
        out.append(f"*👤 {short_name(tsa)}*  ·  " + "  ".join(summary_parts))

        for c in cards:
            out.append(slack_card(c))
            out.append("")

    out.append("_Generated by TSA_CORTEX `scrum_digest_builder.py`_")
    return "\n".join(out)


def render_markdown(health):
    out = []
    out.append(f"# Scrum of Scrums — Milestone Health Digest")
    out.append(f"_{health['date']}_  ·  source of truth: Linear\n")
    out.append(f"**Team rollup:** "
               f"{EMOJI_TXT['red']} {health['by_status'].get('red', 0)} at risk · "
               f"{EMOJI_TXT['yellow']} {health['by_status'].get('yellow', 0)} watch · "
               f"{EMOJI_TXT['green']} {health['by_status'].get('green', 0)} on track · "
               f"{EMOJI_TXT['done']} {health['by_status'].get('done', 0)} shipped (14d)\n")

    by_tsa = defaultdict(list)
    for b in health['buckets']:
        by_tsa[b['tsa_name']].append(b)

    for tsa in sorted(by_tsa.keys()):
        out.append(f"## {short_name(tsa)}\n")
        for c in by_tsa[tsa]:
            m = c['metrics']
            emoji = EMOJI_TXT[c['status']]
            header = c['project'] + (f" — {c['milestone']}" if c['milestone'] else "")
            out.append(f"### {emoji} {header}  _({STATUS_LABEL[c['status']]})_")
            metric_parts = [f"{m['open']} open / {m['total']} total"]
            if m['overdue']:
                metric_parts.append(f"**{m['overdue']} overdue**")
            if m['soon'] and c['status'] != 'red':
                metric_parts.append(f"{m['soon']} due ≤3d")
            if m['earliest_due']:
                metric_parts.append(f"next due `{m['earliest_due']}`")
            out.append("  ·  ".join(metric_parts))
            out.append(f"\n**Why it matters:** {c['impact']}")
            if c['status'] != 'green' and c['reasons']:
                out.append(f"\n**Signal:** {'; '.join(c['reasons'])}")

            # top tickets
            refs = [i for i in c.get('open_issues', []) if i.get('dueDate')]
            refs.sort(key=lambda x: x['dueDate'] or '9999-99-99')
            if refs:
                out.append("")
                for issue in refs[:3]:
                    out.append(f"- [{issue['id']}]({issue['url']}) "
                               f"`{issue['status']}` — {issue['title'][:90]} "
                               f"_(due {issue['dueDate']})_")
            out.append("")
    out.append("---")
    out.append("_Generated by TSA_CORTEX — replaces manual daily agenda posts._")
    return "\n".join(out)


